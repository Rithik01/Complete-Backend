// In the playlist model we will be having a name, description, createdAt,updatedAt, videos(ObjectId[] videos,  owner[ObjectId(users)])
import mongoose from "mongoose";

const playlistSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    owner: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
    },
    videos: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "Video",
      },
    ],
  },
  { timestamps: true }
);

export const PlayList = mongoose.model("Playlist", playlistSchema);
